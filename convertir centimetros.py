# Ejercicio 14: Convertir centímetros a yardas, metros, pies e pulgadas
cm = float(input("Introduce la cantidad en centímetros: "))
metros = cm / 100
yardas = cm / 91.44
pies = cm / 30.48
pulgadas = cm / 2.54
print("Metros:", metros)
print("Yardas:", yardas)
print("Pies:", pies)
print("Pulgadas:", pulgadas)
