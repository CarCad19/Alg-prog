#Uso funciones
import math

x=float(input("Ingrese un numero: "))
seno=math.sin(x)
coseno=math.cos(x)
tangente=math.tan(x)
raiz=math.sqrt(x)
lognat=math.log(x)
log10=math.log10(x)

print("Del numero:", x)
print("Su seno es: ", seno)
print("Su coseno es: ", coseno)
print("Su tangente es: ", tangente)
print("Su raiz cuadrada es: ", raiz )
print("Su logaritmo natural es: ", lognat)
print("Su logaritmo en base 10 es: ", log10)